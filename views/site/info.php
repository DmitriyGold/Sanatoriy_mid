    <div class="row">
        <h5 class="my-3">Статья 79.1. Независимая оценка качества условий оказания услуг медицинскими организациями</h5>
        <br>
    </div>        

    <div class="row">

        <p>

            1. Независимая оценка качества условий оказания услуг медицинскими организациями является 
            одной из форм общественного контроля и проводится в целях предоставления гражданам информации 
            о качестве условий оказания услуг медицинскими организациями, а также в целях повышения
            качества их деятельности. Независимая оценка качества условий оказания услуг медицинскими 
            организациями не осуществляется в целях контроля качества и безопасности медицинской деятельности, 
            а также экспертизы и контроля качества медицинской помощи.<br><br>

            2. Независимая оценка качества условий оказания услуг медицинскими организациями предусматривает 
            оценку условий оказания услуг по таким общим критериям:<br><br>

            открытость и доступность информации о медицинской организации;<br>

            комфортность условий предоставления медицинских услуг, в том числе время ожидания
            предоставления медицинской услуги;<br>

            доброжелательность, вежливость работников медицинской организации;<br>

            удовлетворенность условиями оказания услуг;<br>

            доступность медицинских услуг для инвалидов.<br><br>

            3. Независимая оценка качества условий оказания услуг медицинскими организациями 
            осуществляется в соответствии с положениями настоящей статьи. При проведении независимой
            оценки качества условий оказания услуг медицинскими организациями используется общедоступная 
            информация о медицинских организациях, размещаемая в том числе в форме открытых данных.<br><br>

            4. В целях создания условий для проведения независимой оценки качества условий 
            оказания услуг медицинскими организациями:<br><br>

            1) Общественная палата Российской Федерации по обращению уполномоченного федерального
            органа исполнительной власти не позднее чем в месячный срок со дня получения указанного
            обращения формирует из числа представителей общероссийских общественных организаций, 
            созданных в целях защиты прав и интересов граждан, общероссийских общественных объединений 
            инвалидов общественный совет по проведению независимой оценки качества условий оказания
            услуг медицинскими организациями, участвующими в реализации программы государственных
            гарантий бесплатного оказания гражданам медицинской помощи, учредителем которых является 
            Российская Федерация, и утверждает его состав. Общественная палата Российской Федерации
            информирует уполномоченный федеральный орган исполнительной власти о составе созданного 
            при этом органе общественного совета по проведению независимой оценки качества условий 
            оказания услуг медицинскими организациями.<br><br>


